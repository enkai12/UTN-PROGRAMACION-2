package utn.programacion2.tp1;

/**
 * Este programa demuestra el uso de caracteres de escape
 * para formatear una salida de texto en la consola.
 *
 * @author agu
 */
public class UsoDeEscape {

    public static void main(String[] args) {
        // Usamos una sola cadena de texto (String) con caracteres de escape
        // para lograr el formato deseado en una sola línea de código.
        System.out.println("Nombre: Juan Pérez\nEdad: 30 años\nDirección: \"Calle Falsa 123\"");
    }
}
